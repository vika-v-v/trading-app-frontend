// Definition einer Enumeration zur Beschreibung verschiedener Diagrammtypen
export enum GrafikTyp {
  // Ein Pizzadiagramm, das den Wert der Wertpapiere im Depot darstellt
  PizzadiagrammWertpapierWert = 'Depot-Wertpapier Wert',

  // Ein Pizzadiagramm, das die Menge der Wertpapiere im Depot darstellt
  PizzadiagrammWertpapierMenge = 'Depot-Wertpapier Menge',

  // Ein Liniendiagramm, das den Wertverlauf der Aktien zeigt
  WertverlaufWertpapierWerte = 'Aktien-Wertverlauf',

  // Ein Liniendiagramm, das den Wertverlauf des gesamten Depots zeigt
  WertverlaufDepotwerte = 'Depot-Wertverlauf',

  // Ein Balkendiagramm, das die verschiedenen Arten von Wertpapieren im Depot darstellt
  BardDiagrammWertpapiere = 'Depot-Wertpapierarten'
}
